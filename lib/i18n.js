// Language detection and management
export function getDefaultLanguage() {
  if (typeof window === 'undefined') return 'en';
  
  // Get language from localStorage first
  const saved = localStorage.getItem('vangogh-language');
  if (saved && translations[saved]) return saved;
  
  // Detect from browser
  const browserLang = navigator.language.slice(0, 2);
  return translations[browserLang] ? browserLang : 'en';
}

export function setLanguage(lang) {
  if (typeof window !== 'undefined') {
    localStorage.setItem('vangogh-language', lang);
  }
}

export function getTranslation(key, lang = 'en') {
  const translation = translations[lang] || translations.en;
  return translation[key] || translations.en[key] || key;
} 